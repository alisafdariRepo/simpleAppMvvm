package com.mehrsoft.task_.data.model.comments


import com.google.gson.annotations.SerializedName

data class CommentItem(
    @SerializedName("body")
    val body: String,
    @SerializedName("email")
    val email: String,
    @SerializedName("id")
    val id: Int,
    @SerializedName("name")
    val name: String,
    @SerializedName("postId")
    val postId: Int
)